#include <unistd.h>

int		ft_sqrt(int nb)
{
	int r;

	if (nb <= 0)
		return (0);
	r = 0;
	while (r < nb)
	{
		if (nb == (r * r))
			return (r);
		r++;
	}
	return (0);
}
