#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int		ft_print_alphabet(void)
{
	ft_putstr("abcdefghijklmnopqrstuvwxyz");
	return (0);
}
